package Models;

public class Ranking {
    public String username;
    public int score;

    // Username Get; Set;
    public String getUsername() {
        return this.username;
    }

    public void setUsername(String value) {
        this.username = value;
    }

    // Score Get; Set;
    public int getScore() {
        return this.score;
    }

    public void setScore(int value) {
        this.score = value;
    }
}
